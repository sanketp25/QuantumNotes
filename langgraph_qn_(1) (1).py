class QuantumNotesState(TypedDict):
  file_path: str
  task:str
  query:Optional[str]
  documents: Optional[List[str]]
  summary: Optional[str]
  notes: Optional[str]
  flashcards: Optional[str]
  errors: Optional[List[Exception]]
  chunks: Optional[List[str]]
  embeddings: Optional[List[float]]
